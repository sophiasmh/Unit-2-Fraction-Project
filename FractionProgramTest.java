
public class FractionProgramTest {

	public static void main(String[] args) {
		Fraction frac1 = new Fraction();
		
	// replace "a" with the numerator and "b" with the denominator of the second fraction
	// ex.: for 3/4, the numbers inside the parens would be (3, 4)	
		
		// sum
			frac1.sum(3, 4);
			System.out.println();
			
		// difference	
			frac1.diff(3, 4);
			System.out.println();
			
		// product	
			frac1.prod(3, 4);
			System.out.println();
			
		// quotient	
			frac1.quot(3, 4);
			System.out.println();
			
			

	}

}
